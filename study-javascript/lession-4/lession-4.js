// Variable in java script is loosely-type variable (variable can contain anything)
// is difference in java is strong-type variable

var type = 1; // numeric variable
console.log(type);

type = 'one'; // string variable
console.log(type);

type = 1.1; // decimal variable
console.log(type);

type = true; // boolean variable
console.log(type);

type = null; // null value
console.log(type);